public class Prac13 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println("Pre increment: " + (++x)); // 6
        System.out.println("Post decrement: " + (x--)); // 6
        System.out.println("X's value: " + x); // 5
        System.out.println("Pre decrement: " + (--x)); // 4
        System.out.println("Post increment: " + (x++)); // 4
        System.out.println("X's value: " + x); // 5
    }
}